package com.maxiaohua.genealogy.main.db.base;

import com.maxiaohua.genealogy.fw.core.type.Alias;

/**
 * テーブル・ビューのDTOの共通項目を格納する親クラスです。
 *
 * @author IBM GDC
 */
public class BaseTableDTO {
	/**
	 * ソード条件です。
	 */
	@Alias("ソード条件")
	private String _orderByCondition;

	/**
	 * @return the _orderByCondition
	 */
	public String get_orderByCondition() {
		return _orderByCondition;
	}

	/**
	 * @param _orderByCondition the _orderByCondition to set
	 */
	public void set_orderByCondition(
			String _orderByCondition) {
		this._orderByCondition = _orderByCondition;
	}

	/**
	 * 動的検索条件のWhere分です。
	 */
	@Alias("動的検索条件")
	private String _dynamicWhereCondition;

	/**
	 * @return the _dynamicWhereCondition
	 */
	public String get_dynamicWhereCondition() {
		return _dynamicWhereCondition;
	}

	/**
	 * @param _dynamicWhereCondition the _dynamicWhereCondition to set
	 */
	public void set_dynamicWhereCondition(
			String _dynamicWhereCondition) {
		this._dynamicWhereCondition = _dynamicWhereCondition;
	}
	
	/**
	 * 動的検索条件のWhere分です。
	 */
	@Alias("動的検索条件")
	private String _dynamicWhereCondition2;

	/**
	 * @return the _dynamicWhereCondition
	 */
	public String get_dynamicWhereCondition2() {
		return _dynamicWhereCondition2;
	}

	/**
	 * @param _dynamicWhereCondition the _dynamicWhereCondition to set
	 */
	public void set_dynamicWhereCondition2(
			String _dynamicWhereCondition2) {
		this._dynamicWhereCondition2 = _dynamicWhereCondition2;
	}

	/**
	 * 取得レコード数の条件です。
	 */
	@Alias("取得レコード数の条件")
	private String _fetchFirstCondition;

	/**
	 * 指定した範囲の取得レコード数の条件です。
	 */
	@Alias("指定した範囲の取得レコード数の条件")
	private String _limitCondition;

	/**
	 * 指定した範囲の何行目からの条件です。
	 */
	@Alias("指定した範囲の何行目からの条件")
	private String _offsetCondition;

	/**
	 * @return the _fetchFirstCondition
	 */
	public String get_fetchFirstCondition() {
		return _fetchFirstCondition;
	}

	/**
	 * @param _fetchFirstCondition the _fetchFirstCondition to set
	 */
	public void set_fetchFirstCondition(
			String _fetchFirstCondition) {
		this._fetchFirstCondition = _fetchFirstCondition;
	}

	/**
	 * @return the _limitCondition
	 */
	public String get_limitCondition() {
		return _limitCondition;
	}

	/**
	 * @param _limitCondition the _limitCondition to set
	 */
	public void set_limitCondition(
			String _limitCondition) {
		this._limitCondition = _limitCondition;
	}

	/**
	 * @return the _offsetCondition
	 */
	public String get_offsetCondition() {
		return _offsetCondition;
	}

	/**
	 * @param _offsetCondition the _offsetCondition to set
	 */
	public void set_offsetCondition(
			String _offsetCondition) {
		this._offsetCondition = _offsetCondition;
	}

}
